/**
 * Bundle Convex function sources into self-contained JS modules.
 *
 * The Convex deploy2 API expects pre-bundled JavaScript — it can't resolve
 * bare imports like "convex/server" or "convex/values". This script uses
 * esbuild to bundle each function file so all Convex runtime code is inlined.
 *
 * Output: src/lib/provisioning/convex-bundles.generated.ts
 */

import * as esbuild from 'esbuild'
import { writeFileSync, mkdirSync } from 'fs'
import { resolve, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = resolve(__dirname, '..')
const CONVEX_DIR = resolve(ROOT, 'convex')
const OUT_FILE = resolve(ROOT, 'src/lib/provisioning/convex-bundles.generated.ts')

const entries = [
  { name: 'SCHEMA_BUNDLE', file: 'schema.ts' },
  { name: 'VIDEOS_BUNDLE', file: 'videos.ts' },
  { name: 'REACTIONS_BUNDLE', file: 'reactions.ts' },
  { name: 'HTTP_BUNDLE', file: 'http.ts' },
]

async function bundle() {
  const exports = []

  for (const entry of entries) {
    const result = await esbuild.build({
      entryPoints: [resolve(CONVEX_DIR, entry.file)],
      bundle: true,
      format: 'esm',
      platform: 'browser',
      target: 'esnext',
      // Do NOT externalize convex — we want it bundled in (same as CLI)
      write: false,
      // Match the Convex CLI minification settings
      minifySyntax: true,
      minifyIdentifiers: true,
      minifyWhitespace: false,
      keepNames: true,
      treeShaking: true,
      define: { 'process.env.NODE_ENV': '"production"' },
    })

    const code = result.outputFiles[0].text
    // Escape backticks and backslashes for template literal embedding
    const escaped = code.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$/g, '\\$')
    exports.push(`export const ${entry.name} = \`${escaped}\``)
  }

  const content = [
    '// Auto-generated by scripts/bundle-convex.mjs — do not edit manually',
    '',
    ...exports,
    '',
  ].join('\n')

  mkdirSync(dirname(OUT_FILE), { recursive: true })
  writeFileSync(OUT_FILE, content, 'utf-8')
  console.log(`[bundle-convex] Generated ${OUT_FILE}`)
}

bundle().catch((err) => {
  console.error('[bundle-convex] Failed:', err)
  process.exit(1)
})
